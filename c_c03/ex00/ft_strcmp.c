int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			return (s1[i] - s2[i]);
		}
		i++;
	}
	return (0);
}

#include<stdio.h>

int main(void)
{
	char x[50] = "seun";
	char y[50] = "seunA";
	int i = ft_strcmp(x, y);
	printf("%d\n", i);
}
