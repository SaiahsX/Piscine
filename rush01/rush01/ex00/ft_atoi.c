int	ft_atoi(char *str)
{
	int	i;
	int	val;

	i = 0;
	val = '\0';
	while (*str)
	{
		if (*str == 32 || *str == 43)
			;
		else if (*str == 45)
			i++;
		else if (*str > 47 && *str < 58)
			val = val * 10 + (*str - '0');
		else
			return (val * (-(i % 2)));
		str++;
	}
	return (val * (-(i % 2)));
}
